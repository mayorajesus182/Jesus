select CONVERT(CHAR(10), DATE_ARRIVE, 23),count(*) cant,
'delete  FROM [DBFAO_CRECER].[dbo].[SAP_MESSAGE_TRACE] where DATE_ARRIVE <= '''++CONVERT(CHAR(10), DATE_ARRIVE, 23) ++' 00:00:00.000'++''''
FROM [DBFAO_CRECER].[dbo].[SAP_MESSAGE_TRACE] 
where DATE_ARRIVE >='2018-02-01 00:00:00.000' and DATE_ARRIVE < '2018-07-01 00:00:00.000'
group by CONVERT(CHAR(10), DATE_ARRIVE, 23) 
order by 1;